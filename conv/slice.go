// Strings 具有类型转换能力的[]string类型
type Strings []string

// ToInterfaces 转换为[]interface{}
func (s Strings) ToInterfaces() []interface{} {
	result := make([]interface{}, len(s))
	for i, val := range s {
		result[i] = val
	}
	return result
}

